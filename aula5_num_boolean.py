# conversao de tipos, coerção type convertion, typecasting
# coercion, é o ato de converter um tipo em outro tipos imutaveis 
# e primitivos , como int, float, bool, str 

print(int('1')) # converte str para int
print(float('1')) # converte str para float
print(bool('1')) # converte str para bool
print(str(1)) # converte int para str  
print(float(1)) # converte int para float
print(bool(1)) # converte int para bool
print(str(1.1)) # converte float para str
print(int(1.1)) # converte float para int

