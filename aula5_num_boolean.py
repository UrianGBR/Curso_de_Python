# tipo de dado bool (boleano)
# ao questionar algo, a resposta só pode ser sim(true) ou não(false)
# existem varios operadores para "questionar"
# dentre eles o ==, que é um operador logico que questiona se um valor é igual a outro 

print( 10 == 10) # sim --> true
print( 10 == 11) # não --> false
print(type (10 == 10)) # aqui o tipo de dado retornado é bool(boleano)

