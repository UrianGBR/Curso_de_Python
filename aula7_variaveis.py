# variaveis sao utilizadas para armazenar dados na memoria do computador
# elas podem ser de diferentes tipos, como int, float, str, bool
# para criar uma variavel, basta atribuir um valor a um nome
# PEP 8: inicia variaveis com letras minusculas, pode usar numeros e underlines,
# mas nao pode comecar com numero, e nao pode usar palavras reservadas
# o sinal de = é o operador de atribuição. ele é usado para, atribuir um
# valor a um nome (variavel). 
# Uso: nome_variavel = expressao

nome_completo = "Urian Gabriel"
soma_dois_mais_dois = 2 + 2
print(nome_completo) # Urian Gabriel
print(soma_dois_mais_dois) # 4 

# variaveis sao para tornar o codigo mais legivel 
nome = 'urian'
idade = 27
maior_de_idade = idade >= 18
print('nome:', nome, 'idade:', idade)
print('É maior?', maior_de_idade)