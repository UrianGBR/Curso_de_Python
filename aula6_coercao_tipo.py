# coerção de tipos 
# type convertion , typecasting, coerção
#é o ato de conventer um tipo em outro tipo imutáveis e primitivos
# ex: int, float, bool, str

print(int('1'), type(int('1'))) # converte str para int
print(float('1') + 1) 
print(bool('')) # uma string vazia é considerada False
print(bool(' ')) # uma string com espaço é considerada True
print(str(11) + 'b')