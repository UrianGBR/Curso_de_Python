adicao = 10 + 10
print('adicao:', adicao)  # pode ser numeros de ponto flutuante, numeros inteiros ou misto

subtracao = 10 - 5
print('subtracao:', subtracao) #  

multiplicacao = 10 * 10
print('multiplicacao:', multiplicacao)

divisao = 10 / 2 
print('divisao:', divisao) # o retorno aqui é sempre de float (de ponto flutuante)

divisao_inteira = 10 // 2 
print('divisao_inteira:', divisao_inteira) # nao é sempre que retorno um numero de inteiro , se a divisao for com numero com ponto flutuante o resultado será um float(essa divisao sempre corta as casas decimais)

exponenciacao = 2 ** 10
print('exponenciacao:', exponenciacao) # esse modelo é um numero elevado a outro numero

modulo = 55 % 2 # resto da divisao
print('modulo:', modulo) # este modulo retorna o resto da divisao, na programação é muito utilizado para verificar se um número é par ou ímpar 

print( 10 % 8 == 0) # retorna True se 10 for divisível por 8, caso contrário False

print(16 % 8 == 0) # retorna True se 16 for divisível por 8, caso contrário False

print(10 % 2 == 0) # retorna se o numero for par , se 10 for divisível por 2 (True)

print(15 % 2 == 0) # retorna se o numero for ímpar , se 15 for divisível por 2 (False)
